<?php

for ($l=1; $l<=10; $l++) {
    for ($c=1; $c<=10; $c++) {
        if (
            $l && ($c==10)||
            $l>=2 && ($c==9)||
            $l>=3 && ($c==8)||
            $l>=4 && ($c==7)||
            $l>=5 && ($c==6)||
            $l>=6 && ($c==5)||
            $l>=7 && ($c==4)||
            $l>=8 && ($c==3)||
            $l>=9 && ($c==2)||
            $l>=10 && ($c==1)
            ) {
            echo "#";
        } else {
            echo "&nbsp ";
        }
    }
    echo "<br>";
}

?>