<?php

for ($l=1; $l<=20; $l++) {
    for ($c=1; $c<=20; $c++) {
        if (
            ($c==1 || $c==20) && ($l>=15)||
            ($c>=1 || $c<=20) && ($l==14)||
            ($c==2 || $c==19) && ($l>=7 && $l<=13)||
            ($c==3 || $c==18) && ($l>=3 && $l<=6)||
            ($c==4 || $c==17) && ($l==2)||
            ($c>=5 && $c<=16) && ($l==1)
        ) {
            echo "#";
        } else {
            echo "&nbsp&nbsp";
        }
    }
    echo "<br>";
}

?>