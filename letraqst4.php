<?php 

for ($l=1; $l<=10; $l++) {
    for ($c=1; $c<=20; $c++) {
        if ( 
            $l==10||
            $l==9 && ($c>=2 && $c<=19)||
            $l==8 && ($c>=3 && $c<=18)||
            $l==7 && ($c>=4 && $c<=17)||
            $l==6 && ($c>=5 && $c<=16)||
            $l==5 && ($c>=6 && $c<=15)||
            $l==4 && ($c>=7 && $c<=14)||
            $l==3 && ($c>=8 && $c<=13)||
            $l==2 && ($c>=9 && $c<=12)||
            $l==1 && ($c>=10 && $c<=11)
        ) {
            echo "#"; 
        } else {
            echo "&nbsp";
        }
    }
    echo "<br>";
}

?>